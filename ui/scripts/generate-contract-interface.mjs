import fs from 'node:fs';
import path from 'node:path';
import { execSync } from 'node:child_process';

const repoRoot = path.resolve(process.cwd(), '..');
const abiPath = path.join(process.cwd(), 'src', 'abis', 'agiJobManager.ts');
const raw = fs.readFileSync(abiPath, 'utf8');

const itemRegex = /\{"type":"(function|event|error)","name":"([^"]+)"/g;
const buckets = { function: new Set(), event: new Set(), error: new Set() };
for (const match of raw.matchAll(itemRegex)) {
  buckets[match[1]].add(match[2]);
}

const toSection = (title, values) => {
  const list = [...values].sort((a, b) => a.localeCompare(b));
  return `## ${title}\n\n${list.map((v) => `- \`${v}\``).join('\n')}\n`;
};

const getSafeISOStringFromEpoch = (sourceDateEpoch) => {
  const epoch = Number(sourceDateEpoch);
  if (!Number.isFinite(epoch) || epoch < 0) return null;

  const epochMs = epoch * 1000;
  if (!Number.isFinite(epochMs)) return null;

  const date = new Date(epochMs);
  if (Number.isNaN(date.getTime())) return null;

  return date.toISOString();
};

const getGeneratedStamp = (trackedPaths) => {
  if (process.env.SOURCE_DATE_EPOCH) {
    const stampFromEpoch = getSafeISOStringFromEpoch(process.env.SOURCE_DATE_EPOCH);
    if (stampFromEpoch) return stampFromEpoch;
  }

  try {
    const escaped = trackedPaths.map((trackedPath) => `"${trackedPath}"`).join(' ');
    const command = `git log -1 --format=%ct -- ${escaped}`;
    const epoch = Number(execSync(command, { cwd: repoRoot, encoding: 'utf8' }).trim());
    const stampFromGit = getSafeISOStringFromEpoch(epoch);
    if (stampFromGit) return stampFromGit;
  } catch {
    // fall through to deterministic fallback
  }

  return '1970-01-01T00:00:00.000Z';
};

const stamp = getGeneratedStamp(['ui/src/abis/agiJobManager.ts', 'ui/scripts/generate-contract-interface.mjs']);
const standaloneIdentityRouteNote = `## Standalone identity route compatibility note (v44)\n\nThe canonical standalone artifact (\`ui/agijobmanager_genesis_job_mainnet_2026-03-05-v44.html\`) binds the public Alpha-Agent identity write path directly to:\n\n- Contract: \`FreeTrialSubdomainRegistrarIdentity\`\n- Address: \`0x7811993CbcCa3b8bb35a3d919F3BA59eeFbeAA9a\`\n- Primary public method: \`register(string)\`\n- Recovery methods: \`claimIdentity(string)\`, \`syncIdentityByLabel(string)\`\n- Value semantics: \`0 ETH\` (gas-only call path)\n- Chain posture: Ethereum mainnet (\`chainId = 1\`)\n`;
const markdown = `# AGIJobManager UI Contract Interface\n\nGenerated by \`ui/scripts/generate-contract-interface.mjs\` from \`ui/src/abis/agiJobManager.ts\`.\n\n- Generated at: ${stamp}\n\n${toSection('Functions used by UI', buckets.function)}\n${toSection('Events used by UI', buckets.event)}\n${toSection('Custom errors decoded by UI', buckets.error)}\n## UI compatibility contract\n\nThe UI assumes the listed selectors remain available and semantically stable. If a deployment removes or renames any function/event/error above, regenerate this document and update all read paths, simulation-first write flows, and error remediation mappings before release.\n\n${standaloneIdentityRouteNote}`;

const output = path.join(repoRoot, 'docs', 'ui', 'CONTRACT_INTERFACE.md');
fs.writeFileSync(output, markdown);
console.log(`Wrote ${path.relative(repoRoot, output)}`);
